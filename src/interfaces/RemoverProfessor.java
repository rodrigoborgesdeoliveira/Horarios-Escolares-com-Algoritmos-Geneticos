    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        DataAccessObject.abrirConexao();

        professor = professores.get(jComboBoxProfessores.getSelectedIndex());

        //Remover disciplinas que o professor leciona.
        ArrayList<Disciplina> disciplinas = DataAccessObject.getDisciplinasByIDProfessor(professor.getID());
        for (Disciplina disciplina : disciplinas) {
            //Marcar como aula vaga onde essa disciplina estiver em hor√°rios de turmas.
            DataAccessObject.removeIDDisciplinaFromHorario(disciplina.getID());

            //Remover aulas relacionadas a essa disciplina.
            DataAccessObject.removeAulaByIDDisciplina(disciplina.getID());

            //Remover disciplina da tabela disciplina.
            DataAccessObject.remove(disciplina);
        }

        //Remover professor.
        DataAccessObject.remove(professor);
        
        JOptionPane.showMessageDialog(null, "Professor removido com sucesso!");
        professores.remove(jComboBoxProfessores.getSelectedIndex());
        jComboBoxProfessores.removeItemAt(jComboBoxProfessores.getSelectedIndex());
        
        DataAccessObject.fecharConexao();
    }//GEN-LAST:event_jButtonConfirmarActionPerformed


